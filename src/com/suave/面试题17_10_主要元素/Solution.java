public class Solution {
    public int majorityElement(int[] nums) {
        // å…ˆæ‰¾å‡ºæœ€å¤šçš„é‚£ä¸ªå…ƒç´ 
        int temp = 0;
        int count = 0;
        for (int num : nums) {
            if (count == 0) {
                temp = num;
            }
            if (temp == num) {
                count++;
            } else {
                count--;
            }
        }
        // éå†æ•°é‡
        count = 0;
        for (int num : nums) {
            if (temp == num) {
                count++;
            }
        }
        if (count * 2 > nums.length) {
            return temp;
        }
        return -1;
    }
}
