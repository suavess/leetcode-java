package com.suave.q21_合并两个有序链表;

/**
 * @author Suave
 * @author 2021/3/16 10:48 上午
 */
public class ListNode {
    int val;
    ListNode next;

    ListNode() {
    }

    ListNode(int val) {
        this.val = val;
    }

    ListNode(int val, ListNode next) {
        this.val = val;
        this.next = next;
    }
}
